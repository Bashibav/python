set={'ram',45,36,22,15.22}
print(set,"\n")

#len method
# to print length of the set
print(len(set))

#remove method to remove element of set
set.remove(22)
print("\n",set)

#pop method used to random element from set
set.pop()
print("\n",set)
#clear method make set empty
set.clear()
print("\n",set)